function regexUsename($username)
{
    $usernamePattern = "/^(?=[a-zA-Z0-9._]{5,20}$)(?!.*[_.]{2})[^_.].*[^_.]$/";
    // if(!preg_match($usernamePattern, $username)){
    //     die($username);
    // }
    return preg_match($usernamePattern, $username);
}
function regexPassword($password)
{
    $passwordPattern = "/^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[a-zA-Z]).{6,32}$/m";
    // if(!preg_match($passwordPattern, $password)){
    //     die($password);
    // }
    return preg_match($passwordPattern, $password);
}
function regexPhoneNumber($phoneNumber)
